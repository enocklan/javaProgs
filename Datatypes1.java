public class Datatypes1{
    public static void main(String[] args){
        // Examples of Java data types
        byte b = 100;                  // 1 byte
        short s = 10000;               // 2 bytes
        int age = 25;                  // 4 bytes
        long population = 7800000000L; // 8 bytes
        float price = 19.99f;          // 4 bytes
        double pi = 3.14159;           // 8 bytes
        char grade = 'A';              // 2 bytes
        boolean isJavaFun = true;      // 1 bit

        String name = "John Doe";      // Reference type

        System.out.println("Examples of Java Data Types:");
        System.out.println("byte b = " + b);
        System.out.println("short s = " + s);
        System.out.println("int age = " + age);
        System.out.println("long population = " + population);
        System.out.println("float price = " + price);
        System.out.println("double pi = " + pi);
        System.out.println("char grade = " + grade);
        System.out.println("boolean isJavaFun = " + isJavaFun);
        System.out.println("String name = " + name);
    }
}